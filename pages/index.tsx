import Head from "next/head";
import Image from "next/image";
import styles from "../styles/Home.module.css";
import { v4 as uuidv4 } from "uuid";
import Link from "next/link";
import { SetStateAction, useEffect, useState } from "react";

export default function Home() {
  const [todo, setTodo] = useState("");
  const [sortedTodo, setSortedTodo] = useState<todosType[]>([]);

  type todosType = {
    inputText: string;
    id: string;
    checked: boolean;
    edit: boolean;
  };

  const handleInputTodo = (e: {
    target: { value: SetStateAction<string> };
  }) => {
    setTodo(e.target.value);
  };

  const handleFormSubmit = (e: { preventDefault: () => void }) => {
    e.preventDefault();
    setSortedTodo([
      { id: uuidv4(), inputText: todo.trim(), edit: false, checked: false },
      ...sortedTodo,
    ]);
    setTodo("");
    console.log(sortedTodo);
  };

  return (
    <div className={styles.container}>
      <Head>
        <title>Create Next App</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <main className={styles.main}>
        <h1>TODOリスト</h1>
        <form onSubmit={handleFormSubmit}>
          <input
            type="text"
            value={todo}
            name="todo"
            onChange={handleInputTodo}
          ></input>
          <button type="submit">タスク追加</button>
        </form>

        <ul>
          {sortedTodo.map((todo) => {
            return <li key={todo.id}>{todo.inputText}</li>;
          })}
        </ul>

        <div>
          <button>
            <Link href="/completeTodo">完了したタスク一覧</Link>
          </button>
          <button>
            <Link href="/notCompleteTodo">未完了のタスク一覧</Link>
          </button>
        </div>
      </main>
    </div>
  );
}
